public class Process {
	
    int processNumber;
    int size;
    
    int startIndex;
    int endIndex;

    public Process(int processNumber, int processSize, int startIndex, int endIndex){
        this.processNumber = processNumber;
        this.size = processSize;
        
        this.startIndex = startIndex;
        this.endIndex = endIndex;
    }
	
}
